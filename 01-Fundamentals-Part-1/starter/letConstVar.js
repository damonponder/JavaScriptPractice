// Using let and const (2015)
// Before 2015, using the var keyword was the only way to 
// declare a JavaScript variable.

// The 2015 version of JavaScript (ES6 - ECMAScript 2015) allows 
// the use of the const keyword to define a variable that cannot 
// be reassigned, and the let keyword to define a variable with 
// restricted scope.

// Because it is a little complicated to describe the difference 
// between these keywords, and because they are not supported 
// in older browsers, the first part of this tutorial will most 
// often use var.